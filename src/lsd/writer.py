def _text_confidence(fit) -> str:
    if fit.rule_density == "high" and fit.example_density == "high":
        return "high"
    if fit.rule_density == "low" and fit.procedure_density == "low":
        return "low"
    return "medium"


def _source_policy(url: str, mode: str) -> str:
    visual_line = "- Visual artifacts: none" if mode == "text-first" else \
        "- Visual artifacts: visual/rendered-page.png, visual/tiles/"
    reconcile_line = "- Reconciliation required: yes" if mode != "text-first" else ""

    return f"""# Source Policy

## Canonical source
- URL: {url}
- Active source version: source_v1
- Local artifact: source.md

## Ingestion mode
- Mode: {mode}
{visual_line}
{reconcile_line}

## Update mode
- Mode: monitored
- Promotion policy: manual approval

## Refresh workflow
1. Run `lsd check <package-dir>` to compare normalized hash.
2. Classify change: unchanged / minor / substantial / gone / redirected.
3. Unchanged: no action. Minor: prepare update candidate. Substantial: new
   source version + skill draft, require explicit user decision.
4. Gone: do not overwrite. Use fallback chain below.
5. Never promote without review.

## Fallback order
1. Local source artifact (source.md)
2. Wayback Machine snapshot
3. Secondary archive
4. Previous approved version
"""


def _opportunities_md(opp) -> str:
    lines = ["# Skill Opportunity Map", ""]
    lines.append(f"## Recommended action: {opp.recommended_action}")
    lines.append(f"## Recommended skill type: {opp.recommended_skill_type}")
    lines.append("")
    lines.append("## Candidates")
    lines.append("")
    for c in opp.candidates:
        lines.append(f"### {c.type}")
        lines.append(f"- Confidence: {c.confidence}")
        lines.append(f"- Build timing: {c.build_timing}")
        lines.append(f"- Why: {c.why_fit}")
        if c.needed_extras:
            lines.append(f"- Needed extras: {', '.join(c.needed_extras)}")
        lines.append("")
    return "\n".join(lines)


def _extraction_report(ctx: BuildContext) -> str:
    fetch = ctx.ingestion.fetch
    fit = ctx.source_fit
    opp = ctx.opportunity_map
    mode = ctx.ingestion.mode

    return f"""# Extraction Report

## Decision

Build action: {opp.recommended_action}
Primary skill type: {opp.recommended_skill_type}
Ingestion mode: {mode}

## Source profile

- URL: {fetch.canonical_url}
- Title: {fetch.title}
- Word count: {fetch.word_count}
- Overall fit: {fit.overall_fit}

## Fit scores

- Rule density: {fit.rule_density}
- Procedure density: {fit.procedure_density}
- Example density: {fit.example_density}
- Stability: {fit.stability}
- Specificity: {fit.specificity}
- Composability: {fit.composability}

## Notes

{fit.fit_notes}

## Routing rationale

{ctx.ingestion.routing_notes}

## Maintenance note

Review source changes before promoting any skill update.
Run `lsd check <package-dir>` to detect drift.
"""
