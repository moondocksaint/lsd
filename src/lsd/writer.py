"""Write a complete LSD skill package to disk."""

from __future__ import annotations

import json
from datetime import datetime, timezone
from pathlib import Path

from lsd import __version__ as lsd_version
from lsd.compiler import compile_skill, compile_skill_multi
from lsd.models import BuildContext, MultiSourceBuildContext
from lsd.normaliser import content_hash, normalise


def write_package(ctx: BuildContext) -> Path:
    """Write all package files to ctx.output_dir. Returns the output directory."""
    out = ctx.output_dir
    out.mkdir(parents=True, exist_ok=True)

    generated_at = datetime.now(timezone.utc).isoformat()
    ctx.generated_at = generated_at

    fetch = ctx.ingestion.fetch
    mode = ctx.ingestion.mode
    opp = ctx.opportunity_map

    normalised = normalise(fetch)
    norm_hash = content_hash(normalised)

    # Compile SKILL.md and capture the model that did it
    skill_content, compiler_model = compile_skill(ctx)
    (out / "SKILL.md").write_text(skill_content)

    # source.md
    (out / "source.md").write_text(normalised)

    # metadata.json — includes compiler_model
    (out / "metadata.json").write_text(
        json.dumps(_build_metadata(ctx, norm_hash, generated_at, compiler_model), indent=2)
    )

    # source-policy.md
    (out / "source-policy.md").write_text(_source_policy(fetch.canonical_url, mode))

    # skill-opportunities.md
    (out / "skill-opportunities.md").write_text(_opportunities_md(opp))

    # extraction-report.md
    (out / "extraction-report.md").write_text(_extraction_report(ctx))

    # CHANGELOG.md
    (out / "CHANGELOG.md").write_text(
        f"# Changelog\n\n## 0.1.0 — {generated_at[:10]}\n- Initial package generated by LSD.\n"
    )

    # README.md — first-class package output (agent entry point for versioning)
    (out / "README.md").write_text(
        _readme(ctx, norm_hash, generated_at, compiler_model)
    )

    # visual/ placeholder for hybrid/visual-first
    if mode in ("hybrid", "visual-first"):
        visual_dir = out / "visual" / "tiles"
        visual_dir.mkdir(parents=True, exist_ok=True)
        (out / "visual" / ".gitkeep").touch()

    return out


def write_multi_package(ctx: MultiSourceBuildContext) -> Path:
    """Write multi-source build artifacts to ctx.output_dir.

    Compiles SKILL.md (with compiler_model) inside this function so the
    model id is available for metadata.json and README.md. Retrieval
    backend is taken from ctx._retrieval_backend / ctx._retrieval_index
    if available (set by pipeline.build_multi).
    """
    out = ctx.output_dir
    out.mkdir(parents=True, exist_ok=True)

    generated_at = datetime.now(timezone.utc).isoformat()

    # Per-source files
    for entry in ctx.sources:
        path = out / f"source-{entry.index}.md"
        path.write_text(entry.normalised, encoding="utf-8")

    # sources-index.md
    lines: list[str] = [
        "# Sources Index\n\n",
        f"Built: {generated_at}\n\n",
        f"Sources: {len(ctx.sources)}\n\n",
        "| # | URL | Type | Mode | Overall Fit |\n",
        "|---|-----|------|------|-------------|\n",
    ]
    for e in ctx.sources:
        source_type = e.source_type if isinstance(e.source_type, str) else str(e.source_type)
        ingestion_mode = e.ingestion_mode if isinstance(e.ingestion_mode, str) else str(e.ingestion_mode)
        overall_fit = getattr(e.fit, "overall_fit", "—")
        lines.append(
            f"| {e.index} | {e.url} | {source_type} | {ingestion_mode} | {overall_fit} |\n"
        )
    (out / "sources-index.md").write_text("".join(lines), encoding="utf-8")

    # conflicts.md
    conflict_lines: list[str] = [
        "# Conflict Report\n\n",
        f"{ctx.conflict_report.summary}\n\n",
    ]
    if ctx.conflict_report.has_blocking_conflicts:
        conflict_lines.append(
            "> **Warning:** High-severity contradictions detected. "
            "Review before using this skill.\n\n"
        )
    if not ctx.conflict_report.conflicts:
        conflict_lines.append("No conflicts detected.\n")
    else:
        for i, c in enumerate(ctx.conflict_report.conflicts, 1):
            conflict_lines.extend([
                f"## Conflict {i}: {c.kind.title()} [{c.severity.upper()}]\n\n",
                f"**Sources involved:** {', '.join(str(s) for s in c.source_indices)}\n\n",
                f"**Description:** {c.description}\n\n",
                f"**Suggestion:** {c.suggestion}\n\n",
            ])
    (out / "conflicts.md").write_text("".join(conflict_lines), encoding="utf-8")

    # skill-opportunities.md
    opp = ctx.combined_opportunities
    (out / "skill-opportunities.md").write_text(_opportunities_md(opp), encoding="utf-8")

    # Compile SKILL.md — retrieval backend may be pre-attached by build_multi()
    ret_backend = getattr(ctx, "_retrieval_backend", None)
    ret_index = getattr(ctx, "_retrieval_index", None)
    skill_content, compiler_model = compile_skill_multi(
        ctx,
        retrieval_backend=ret_backend if ret_index is not None else None,
    )
    (out / "SKILL.md").write_text(skill_content, encoding="utf-8")

    # metadata.json — v0.5 schema, compiler_model now populated
    meta = _build_metadata_multi(ctx, generated_at, compiler_model)
    (out / "metadata.json").write_text(json.dumps(meta, indent=2), encoding="utf-8")

    # README.md — agent entry point for versioning (multi-source variant)
    (out / "README.md").write_text(
        _readme_multi(ctx, generated_at, compiler_model), encoding="utf-8"
    )

    # index.md
    index_lines: list[str] = [
        "# LSD Multi-Source Build\n\n",
        f"Sources: {len(ctx.sources)}\n\n",
        "## Files\n\n",
    ]
    for e in ctx.sources:
        index_lines.append(f"- [source-{e.index}.md](source-{e.index}.md) — {e.url}\n")
    index_lines.extend([
        "- [sources-index.md](sources-index.md)\n",
        "- [conflicts.md](conflicts.md)\n",
        "- [skill-opportunities.md](skill-opportunities.md)\n",
        "- [metadata.json](metadata.json)\n",
        "- [README.md](README.md)\n",
        "- [SKILL.md](SKILL.md)\n",
    ])
    (out / "index.md").write_text("".join(index_lines), encoding="utf-8")

    return out


