def _build_metadata(
    ctx: BuildContext,
    norm_hash: str,
    generated_at: str,
    compiler_model: str | None = None,
) -> dict:
    fetch = ctx.ingestion.fetch
    mode = ctx.ingestion.mode
    fit = ctx.source_fit
    opp = ctx.opportunity_map

    visual_artifacts: list[str] = []
    if mode in ("hybrid", "visual-first"):
        visual_artifacts = ["visual/rendered-page.png", "visual/tiles/"]

    return {
        "package": {
            "name": opp.recommended_skill_type.replace("_", " ").title(),
            "version": "0.1.0",
            "generated_at": generated_at,
            "lsd_version": lsd_version,
            "compiler_model": compiler_model,
            "output_mode": "full_package",
        },
        "source_dependency": {
            "canonical_url": fetch.canonical_url,
            "source_title": fetch.title,
            "active_source_version": "source_v1",
            "local_source_file": "source.md",
            "last_checked_at": generated_at,
            "last_successful_fetch_at": generated_at,
            "normalized_hash": norm_hash,
            "archive_urls": [],
            "update_mode": "monitored",
            "promotion_policy": "manual_approval",
            "fallback_order": [
                "local_copy", "wayback_snapshot",
                "secondary_archive", "previous_version",
            ],
        },
        "ingestion": {
            "mode": mode,
            "text_extraction_confidence": _text_confidence(fit),
            "visual_extraction_enabled": mode in ("hybrid", "visual-first"),
            "reconciliation_required": mode in ("hybrid", "visual-first"),
            "visual_artifacts": visual_artifacts,
            "routing_notes": ctx.ingestion.routing_notes,
        },
        "source_fit": {
            "overall_fit": fit.overall_fit,
            "rule_density": fit.rule_density,
            "procedure_density": fit.procedure_density,
            "example_density": fit.example_density,
            "stability": fit.stability,
            "specificity": fit.specificity,
            "composability": fit.composability,
            "fit_notes": fit.fit_notes,
        },
        "opportunity_summary": {
            "recommended_action": opp.recommended_action,
            "recommended_skill_type": opp.recommended_skill_type,
            "candidate_skill_types": [
                {
                    "type": c.type,
                    "confidence": c.confidence,
                    "build_timing": c.build_timing,
                    "why_fit": c.why_fit,
                    "needed_extras": c.needed_extras,
                }
                for c in opp.candidates
            ],
        },
        "governance": {
            "criticality": "medium",
            "review_required_for_promotion": True,
            "auto_update_threshold": "never",
            "change_classification_policy": "unchanged/minor/substantial/gone/redirected",
            "policy_notes": "Review all source changes before promoting an updated skill.",
        },
        "artifacts": {
            "readme_file": "README.md",
            "skill_file": "SKILL.md",
            "source_file": "source.md",
            "source_policy_file": "source-policy.md",
            "opportunity_map_file": "skill-opportunities.md",
            "extraction_report_file": "extraction-report.md",
            "changelog_file": "CHANGELOG.md",
            "visual_files": visual_artifacts,
        },
    }


def _build_metadata_multi(
    ctx: MultiSourceBuildContext,
    generated_at: str,
    compiler_model: str | None,
) -> dict:
    """Build metadata.json for a multi-source package. compiler_model is now populated."""
    opp = ctx.combined_opportunities
    return {
        "package": {
            "version": "0.3.0",
            "generated_at": generated_at,
            "lsd_version": lsd_version,
            "output_mode": "multi_source",
            "compiler_model": compiler_model,   # populated by compile_skill_multi()
        },
        "source_dependencies": [
            {
                "index": e.index,
                "url": e.url,
                "source_type": e.source_type if isinstance(e.source_type, str) else str(e.source_type),
                "ingestion_mode": e.ingestion_mode if isinstance(e.ingestion_mode, str) else str(e.ingestion_mode),
                "normalized_hash": content_hash(e.normalised),
                "last_checked_at": generated_at,
                "overall_fit": getattr(e.fit, "overall_fit", "unknown"),
                "fallback_order": [
                    "local_copy", "wayback_snapshot",
                    "secondary_archive", "previous_version",
                ],
            }
            for e in ctx.sources
        ],
        "conflict_summary": ctx.conflict_report.summary,
        "has_blocking_conflicts": ctx.conflict_report.has_blocking_conflicts,
        "opportunity_summary": {
            "recommended_action": opp.recommended_action,
            "recommended_skill_type": opp.recommended_skill_type,
            "candidate_count": len(opp.candidates),
        },
        "governance": {
            "review_required_for_promotion": True,
            "auto_update_threshold": "never",
            "change_classification_policy": "unchanged/minor/substantial/gone/redirected",
        },
        "artifacts": {
            "readme_file": "README.md",
            "skill_file": "SKILL.md",
            "index_file": "index.md",
            "sources_index_file": "sources-index.md",
            "conflicts_file": "conflicts.md",
            "opportunity_map_file": "skill-opportunities.md",
            "metadata_file": "metadata.json",
        },
    }


